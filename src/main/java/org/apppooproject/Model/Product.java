package org.apppooproject.Model;

public class Product {
    private long productId;
    private String name;
    private double price;
    private int stock;

    public long getProductId() {
        return productId;
    }

    public double getPrice() {
        return price;
    }

    public int getStock() {
        return stock;
    }

    public String getName() {
        return name;
    }

}
